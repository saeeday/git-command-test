package com.aypara.example;

public class A {
	private int x;
	private int y;
	private String name;
	public A(int x,int y ,String n) {
		this.name=n;
		this.x=x;
		this.y=y;
	}
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	public String getName()
	{
		return name;
	}
}
